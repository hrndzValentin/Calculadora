package Calculadora;

public class Metodo {
	
	double resultado;
	
	
	public double Sumar(double a, double b) {
		 resultado = a + b;
		 return resultado;
		 
	}
	public double Restar(double a, double b) {
		 resultado = a - b;
		 return resultado;
		 
	}
	public double Multiplicar(double a, double b) {
		 resultado = a * b;
		 return resultado;
		 
	}
	public double Dividir(double a, double b) {
		 resultado = a + b;
		 return resultado;
		 
	}
}
